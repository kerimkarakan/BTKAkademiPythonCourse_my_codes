# Çarpma, toplama, çıkarma, bölme (matematiksel işlemler ve veri tipleri arasındaki farklılıklar)

print(35*87) # * operatörü çarpma işlemi yapar

print(35*87.7)
print(35*87.0) # float ile çarptığımız bir sayının sounuc float olur her zaman

# / operatörü bölme işlemi yapar ve sonucu her zaman float yani ondalıklı sayı olur (bölündüğü sayı float olsada olmasa da)
print(35/5)
print(35/5.0)

print(35+5.0) # + oparatörü toplama işlemi yapar

print(35-5.0) # - oparatörü çıkarma işlemi yapar

print("35" + "90") # + operatörü aynı zamanda stringler arasında birleştirme yapar
print("kerim" + "karakan")

print("kerimkerim" * 2) # * operatörü stringlerde çoğaltma işlemi yapar
