# mantıksal operatörler (and ve or operatörleri)
# karşılaştırma işlemleri sonuçlarını bağlamak için kullanılır
#True veya False ile sonuçlarnırlar
# ve için and, veya için or kullanılır
# not() "değil" amacıyla yada "tersine" amacıyla kullanılır

print((56==87) and (74>=45)) # bu yaptığımız işlemlerin en az bir tanesi bile yanlışsa sonuç False çıkar (and operatörü için)

print((56==87) or (74>=45)) # bu yaptığımız işlemlerde en az bir tanesi bile doğruysa sonuç True çıkar (or operatörü için)

print(not(56==55)) # olumsuz bir sonuç aradığımız için burda elde edilen veri True olsun (yani yanlış olana yanlış diyince bu doğru oluyor)



