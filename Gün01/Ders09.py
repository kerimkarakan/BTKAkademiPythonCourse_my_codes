# string ve bool ifadelerde eşitlik kontrolü amacıyla kullanılır 
# sonuçlar her zaman True veya False olur

print(9 == 8) # eğer eşitlik varsa sonucumuz "True" eğer eşitlik yoksa sonucumuz "False" olacaktır (a değeri b değerine eşit mi diye soru souryoruz)

print(9!=8) # burda eğer eşitlik varsa sonucumuz Flase olur eğer eşitlik yoksa sonucumuz True olur (a değeri b değerine eşit değil mi diye soru souryoruz)

print("8"== 8)

print("5 sayısı 8 den küçük mü ?" , 5 < 8)
