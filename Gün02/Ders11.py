# gün değerine göre haftanın gün sayısı

gun = input("gün değeri giriniz:")

if gun == "pazartesi" :
    print("birinci gün")
elif gun == "salı" :
    print("ikinci gün")
elif gun == "çarşamba" :
    print("üçüncü gün")
elif gun == "perşembe" :
    print("dördüncü gün")
elif gun == "cuma" :
    print("beşinci gün")
elif gun == "cumartesi" :
    print("altıncı gün")
elif gun == "pazar" :
    print("yedinci gün")
else: 
    print("hatalı gün girdiniz.")    # hoca sadece pazartesi ve salı yaptığı gerisini ben yaptım